n = int(input())

a = [1] * 10
a[0] = 0
a[8] = 0

for _ in range(n - 1):
    b = [0] * 10
    b[0] = a[4] + a[6]
    b[1] = a[6] + a[8]
    b[2] = a[7] + a[9]
    b[3] = a[4] + a[8]
    b[4] = a[3] + a[9] + a[0]
    b[5] = 0
    b[6] = a[1] + a[7] + a[0]
    b[7] = a[2] + a[6]
    b[8] = a[1] + a[3]
    b[9] = a[2] + a[4]
    a = b.copy()
print(sum(a) % 1000000000)
